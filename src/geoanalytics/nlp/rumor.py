"""F4 (Волна 4): фактологичность новости — fact / rumor / opinion.

Зачем: телеграм-каналы льют не только факты, но и слухи («по данным источников»),
прогнозы и авторские мнения/сатиру (кейс «Трамп 🗣» из CAPITALIST_2033 с
significance 0.85). Метка фактологичности понижает надёжность болтливых источников
(F7) и опускает спекулятивное в ранжировании сводки.

v1 — детерминированные правила (как F5 numeric): статей с явными маркерами мало,
дистилляция (LLM-золото → full-FT tiny2) откладывается до накопления телеграм-корпуса.
Чистая функция `classify_factuality` — основной предмет тестов.

Фактологичность ортогональна времени: прогноз (F3 `forecast`) — это НЕ слух, а
отдельная ось (аналитический прогноз из надёжного канала ценен). Поэтому forecast
сам по себе не делает новость слухом — он лишь повышает уверенность, КОГДА уже есть
маркер неподтверждённости. Дефолт без маркеров — `fact` с низкой уверенностью:
новостной официоз и есть основной фон, метка не перекрашивает обычные новости.
"""

from __future__ import annotations

import re

FACT = "fact"
RUMOR = "rumor"
OPINION = "opinion"
LABELS = (FACT, RUMOR, OPINION)

# Маркеры неподтверждённости/слуха (не свершившийся, не атрибутированный факт).
# Precision-first: берём только однозначно «неофициальные/со слов источников»
# обороты. Пустую модальность намерения («планирует/обсуждает/может») НЕ относим к
# слуху — это нормальная фактура корпоративных планов; будущее ловит F3 forecast.
# По границам слов, регистронезависимо; корни без окончаний ловят словоформы.
_RUMOR_MARKERS = (
    r"по\s+данным\s+источник",
    r"сообща\w*\s+источник",
    r"источник\w*\s+(?:сообща|расска|утвержда)",
    r"по\s+(?:нашей\s+)?информации",
    r"по\s+слухам",
    r"\bслух\w*",
    r"инсайдер\w*",
    r"неофициальн\w*",
    r"\bякобы\b",
    r"предположительн\w*",
    r"не\s+подтвержд\w*",
    r"\bне\s+исключ\w*",
    r"СМИ\s+сообща\w*",
    r"анонимн\w*\s+источник",
)

# Маркеры авторского мнения/оценки/сатиры. Эмодзи речи (🗣) — мягкий сигнал.
_OPINION_MARKERS = (
    r"на\s+мой\s+взгляд",
    r"\bпо-моему\b",
    r"\bсчита\w*,?\s+что",
    r"\bмнени\w*",
    r"\bимхо\b",
    r"как\s+по\s+мне",
    r"\bкажется,",
    r"🗣",
)

_RUMOR_RE = [re.compile(p, re.IGNORECASE) for p in _RUMOR_MARKERS]
_OPINION_RE = [re.compile(p, re.IGNORECASE) for p in _OPINION_MARKERS]


def _count(patterns: list[re.Pattern], text: str) -> int:
    return sum(1 for p in patterns if p.search(text))


def classify_factuality(
    text: str, *, temporal_status: str | None = None
) -> tuple[str, float]:
    """Возвращает (метка ∈ LABELS, уверенность ∈ [0,1]). Чистая функция.

    Приоритет: мнение/сатира > слух/прогноз > факт. Несколько маркеров одного
    класса повышают уверенность. Без маркеров — fact с низкой уверенностью.
    """
    if not text:
        return FACT, 0.3
    op = _count(_OPINION_RE, text)
    ru = _count(_RUMOR_RE, text)
    forecast = temporal_status == "forecast"

    # Мнение перебивает: явная авторская оценка важнее наличия фактуры в том же тексте.
    if op:
        return OPINION, round(min(0.5 + 0.2 * op, 0.95), 3)
    # Слух — только при явных маркерах неподтверждённости. Прогноз (F3) лишь повышает
    # уверенность поверх маркера (неподтверждённое + о будущем), но сам слухом не делает.
    if ru:
        conf = 0.55 + 0.15 * ru + (0.15 if forecast else 0.0)
        return RUMOR, round(min(conf, 0.95), 3)
    return FACT, 0.5
