"""F10 (Волна 4): роутер новость↔прогноз для брокерских каналов.

Брокерские каналы (Сбер/Т-/БКС Инвестиции) льют и новости, и прогнозы (целевая цена,
ожидаемый дивиденд, рекомендация покупать/держать/продавать) вперемешку. Прогноз нельзя
лить в общий сентимент как факт рынка («брокеры торгуют книгу» — позитивный уклон), и его
ценность иная: это ОЖИДАНИЕ, которое позже сравнивается с фактом (surprise, будущая работа).

`is_forecast_post` разводит пост: True → forecast-путь (извлечение целей в таблицу
`forecasts`, статья помечается `is_forecast` и не идёт в общий сентимент-грунт); False —
обычный новостной NLP-каскад.

v1 — детерминированные правила (как F4/F5), precision-first: ложная пометка прогнозом хуже
пропуска. Дистилляция (LLM-золото → full-FT) и surprise-классификатор — позже, когда корпус
прогнозов накопится (методология «данные→модель»). Чистая функция — основной предмет тестов.
"""

from __future__ import annotations

import re

# Брокерские каналы из tg.txt (ключ = source_ref). Здесь живут прогнозы вперемешку с
# новостями; для них «вооружены» и мягкие маркеры ожидания.
BROKER_CHANNELS = frozenset(
    {"SberInvestments", "tb_invest_official", "bcs_world_of_investments"}
)

# Сильные маркеры аналитического прогноза — однозначная брокерская речь о цели/рекомендации.
# Срабатывают на ЛЮБОМ канале: обычный новостной официоз так не пишет.
_STRONG_MARKERS = (
    r"целев(?:ая|ой|ую)\s+цен",
    r"\bтаргет\w*",
    r"справедлив(?:ая|ой|ую)\s+(?:цен|стоимост)",
    r"потенциал\s+рост",
    r"\bапсайд\w*",
    r"\bupside\b",
    r"рекоменд\w*\s+(?:покупа|держа|продава)",
    r"рекомендаци\w*[:\s]+(?:покупа|держа|продава)",
    r"взгляд\s+на\s+акци",          # «позитивный взгляд на акции …»
)

# Мягкие маркеры ожидания — на брокерском канале + (F3 forecast или число) дают прогноз.
# Сами по себе НЕ решают: «ЦБ ожидает инфляцию» — новость, не брокерский таргет.
_SOFT_MARKERS = (
    r"\bожида\w*",
    r"\bпрогнозиру\w*",
    r"\bпрогноз\b",
    r"\bжд[её]м\b",
    r"по\s+нашим\s+оценк",
    r"\bоценива\w*\s+потенциал",
)

_STRONG_RE = [re.compile(p, re.IGNORECASE) for p in _STRONG_MARKERS]
_SOFT_RE = [re.compile(p, re.IGNORECASE) for p in _SOFT_MARKERS]
_HAS_NUM = re.compile(r"\d")


def _any(patterns: list[re.Pattern], text: str) -> bool:
    return any(p.search(text) for p in patterns)


def is_forecast_post(
    title: str,
    body: str | None = None,
    *,
    channel: str | None = None,
    temporal_status: str | None = None,
) -> bool:
    """True — пост является прогнозом (forecast-путь), False — обычная новость.

    Чистая функция, precision-first:
    - сильные маркеры (целевая цена/таргет/рекомендация) → прогноз на любом канале;
    - на брокерском канале — ещё и мягкие маркеры ожидания вместе с количественным
      сигналом (F3 `forecast` или наличие числа), чтобы «ожидаем сильный отчёт» с целью
      попадало, а «глава ЦБ ожидает» — нет.
    """
    if not title:
        return False
    text = f"{title}. {body or ''}"
    if _any(_STRONG_RE, text):
        return True
    if channel in BROKER_CHANNELS and _any(_SOFT_RE, text):
        if temporal_status == "forecast" or _HAS_NUM.search(text):
            return True
    return False
