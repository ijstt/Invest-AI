"""Классификация новостей по макро-темам (санкции, инфляция, ставка, курс, нефть…).

Темы — не упоминания сущностей, а тематические маркеры текста (по ключевым словам).
Используются для тематической привязки новостей (ArticleEntity типа macro_theme) и
тематических алертов. Таксономия здесь — единый источник правды; та же карта сидится
в таблицу macro_themes (storage/seed.py) для связей по id.
"""

from __future__ import annotations

import re

# Тема → список regex-маркеров (по нормализованному тексту в нижнем регистре).
THEME_KEYWORDS: dict[str, list[str]] = {
    "Санкции": [r"санкц", r"эмбарго", r"ограничени\w* (на )?поставк", r"чёрн\w* списк"],
    "Инфляция": [r"инфляц", r"подорожани", r"рост цен", r"индекс цен"],
    "Ключевая ставка": [r"ключев\w* ставк", r"ставк\w* цб", r"\bдкп\b",
                        r"денежно-кредитн", r"(подня|повыс|сниз|сохран|поднял)\w* .{0,14}ставк"],
    "Курс валют": [r"курс\w* (рубл|доллар|евро|юан|валют)", r"девальв",
                   r"рубл\w* (подешев|подорож|укреп|ослаб|просел)",
                   r"(подешев|подорож|укреп|ослаб)\w* (рубл|к доллар|к евро)"],
    "Нефть и сырьё": [r"\bнефт", r"\bbrent\b", r"\bgaz\b|\bгаз\b", r"баррел", r"\bопек\b"],
    "Геополитика": [r"переговор", r"конфликт", r"саммит", r"геополит", r"спецоперац"],
}


def classify_themes(text: str) -> list[str]:
    """Темы, затронутые в тексте (по маркерам). Пустой список — нет явных тем. Чистая."""
    low = (text or "").lower()
    return [name for name, pats in THEME_KEYWORDS.items()
            if any(re.search(p, low) for p in pats)]
